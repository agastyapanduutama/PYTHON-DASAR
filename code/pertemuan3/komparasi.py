# Operator Komparasi
# Lebih besar, lebih kecil, lebih besar sama dengan, lebih kecil sama dengan, sama dengan, tidak sama dengan
# >, <, >=, <=, ==, !=

a = 10
b = 20

# Lebih Besar
hasil = a > b
print(f"apakah {a} > {b} = {hasil}")

# Lebih Kecil
hasil = a < b
print(f"apakah {a} < {b} = {hasil}")

# Lebih besar sama dengan
hasil = a >= b
print(f"apakah {a} >= {b} = {hasil}")

# Lebih kecil sama dengan
hasil = a <= b
print(f"apakah {a} <= {b} = {hasil}")

# Sama dengan
hasil = a == b
print(f"apakah {a} == {b} = {hasil}")

# Tidak sama dengan
hasil = a != b
print(f"apakah {a} != {b} = {hasil}")