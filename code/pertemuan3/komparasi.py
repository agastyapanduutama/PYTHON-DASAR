# Komparasi
# >, <,, >=, <=, ==, !=
a = 10
b = 20

hasil = a > b
print(f"apakah {a} > {b} = {hasil}")

hasil = a < b
print(f"apakah {a} < {b} = {hasil}")

hasil = a >= b
print(f"apakah {a} >= {b} = {hasil}")

hasil = a <= b
print(f"apakah {a} <= {b} = {hasil}")

hasil = a == b
print(f"apakah {a} == {b} = {hasil}")

hasil = a != b
print(f"apakah {a} != {b} = {hasil}")