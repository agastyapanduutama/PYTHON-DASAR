# Operator Aritmatika
nilai1 = 10
nilai2 = 4

# Pertambahan
print("-------Pertambahan-------")
hasil = nilai1+nilai2
print(f"Hasil dari {nilai1} + {nilai2} adalah {hasil}")

# Pengurangan
print("-------Pengurangan-------")
hasil = nilai1-nilai2
print(f"Hasil dari {nilai1} - {nilai2} adalah {hasil}")

# Perkalian
print("-------Perkalian-------")
hasil = nilai1*nilai2
print(f"Hasil dari {nilai1} x {nilai2} adalah {hasil}")

# Pembagian
print("-------Pembagian-------")
hasil = nilai1/nilai2
print(f"Hasil dari {nilai1} / {nilai2} adalah {hasil}")

# Modulus
print("-------Modulus-------")
hasil = nilai1%nilai2
print(f"Hasil dari {nilai1} ** {nilai2} adalah {hasil}")

# Eksponen/Pangkat
print("-------Eksponen/Pangkat-------")
hasil = nilai1**nilai2
print(f"Hasil dari {nilai1} % {nilai2} adalah {hasil}")

# Floor Division
print("-------Floor Division-------")
hasil = nilai1//nilai2
print(f"Hasil dari {nilai1} // {nilai2} adalah {hasil}")
